package com.society.util;

import java.util.Date;

public class DateWrapper {
	Date from;
	Date to;
	Date on;
	public Date getOn() {
		return on;
	}
	public void setOn(Date on) {
		this.on = on;
	}
	public Date getFrom() {
		return from;
	}
	public void setFrom(Date from) {
		this.from = from;
	}
	public Date getTo() {
		return to;
	}
	public void setTo(Date to) {
		this.to = to;
	}
}
